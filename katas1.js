function oneThroughTwenty() {
  let meuRetorno1 = []
  let incre1 = 1
  while (incre1 <= 20){
    meuRetorno1.push(incre1)
    incre1++
  }

  return meuRetorno1
}

console.log(oneThroughTwenty())

